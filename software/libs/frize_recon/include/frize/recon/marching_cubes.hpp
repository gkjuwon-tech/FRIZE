// TSDF → Mesh. iso=0 등치면. 미탐사 큐브는 스킵(프런티어 홀).
inline Mesh marching_cubes(const TsdfVolume& vol) {
    using namespace mc_tables;
    Mesh m;
    std::unordered_map<int64_t,uint32_t> vmap;   // 엣지 단위 정점 캐시(공유→법선 매끈)
    auto edge_key=[&](int i,int j,int k,int e)->int64_t{
        // 엣지를 (셀,엣지)로 정규화 — 인접 큐브가 같은 엣지 정점 공유
        return (((int64_t)i*2048+j)*2048+k)*16+e;
    };
    const int nx=vol.nx(), ny=vol.ny(), nz=vol.nz();

    for (int k=0;k<nz-1;++k) for (int j=0;j<ny-1;++j) for (int i=0;i<nx-1;++i){
        float sdf[8]; bool known=true;
        for (int ci=0;ci<8;++ci){
            const TsdfCell& c=vol.at(i+corner[ci][0], j+corner[ci][1], k+corner[ci][2]);
            if (c.w<=0.0f){ known=false; break; }   // 미탐사 → 큐브 스킵
            sdf[ci]=c.sdf;
        }
        if (!known) continue;
        int cubeindex=0;
        for (int ci=0;ci<8;++ci) if (sdf[ci]<0.0f) cubeindex|=(1<<ci);
        int edges=edgeTable[cubeindex];
        if (edges==0) continue;

        uint32_t evid[12];
        for (int e=0;e<12;++e){
            if (!(edges&(1<<e))) continue;
            int c0=edge2corner[e][0], c1=edge2corner[e][1];
            int i0=i+corner[c0][0], j0=j+corner[c0][1], k0=k+corner[c0][2];
            int i1=i+corner[c1][0], j1=j+corner[c1][1], k1=k+corner[c1][2];
            // 정규화 키(작은 코너 기준)
            int64_t key = (i0<i1||(i0==i1&&(j0<j1||(j0==j1&&k0<=k1))))
                ? edge_key(i0,j0,k0,e) : edge_key(i1,j1,k1,e);
            auto it=vmap.find(key);
            if (it!=vmap.end()){ evid[e]=it->second; continue; }
            float s0=sdf[c0], s1=sdf[c1];
            float t = (std::fabs(s0-s1)>1e-6f) ? s0/(s0-s1) : 0.5f;
            Vec3f p0=vol.world(i0,j0,k0), p1=vol.world(i1,j1,k1);
            Vec3f vp = p0 + (p1-p0)*t;
            uint32_t id=(uint32_t)m.v.size();
            m.v.push_back(vp);
            m.n.push_back({0,0,0});
            m.temp.push_back(vol.temp_trilerp(vp));
            vmap[key]=id; evid[e]=id;
        }
        const int* tri=triTable[cubeindex];
        for (int t=0;tri[t]!=-1;t+=3){
            uint32_t a=evid[tri[t]], b=evid[tri[t+1]], c=evid[tri[t+2]];
            m.idx.push_back(a); m.idx.push_back(b); m.idx.push_back(c);
            Vec3f e1=m.v[b]-m.v[a], e2=m.v[c]-m.v[a];
            Vec3f fn{e1.y*e2.z-e1.z*e2.y, e1.z*e2.x-e1.x*e2.z, e1.x*e2.y-e1.y*e2.x};
            m.n[a]=m.n[a]+fn; m.n[b]=m.n[b]+fn; m.n[c]=m.n[c]+fn;
        }
    }
    for (auto& nn:m.n){ float L=norm(nn); if(L>1e-9f) nn=nn*(1.0f/L); else nn={0,0,1}; }
    m.colorize();
    return m;
}

